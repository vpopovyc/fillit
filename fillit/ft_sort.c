#include "ft_header.h"

char	*ft_map(int n)
{
	char	*map;
	int		i;
	int		j;

	j = n;
	i = (n * n) + n;
	map = ft_strnew(i);
	while (j)
	{
		map[n] = '\n';
		n += n;
	}
	return(map); 
}