#include "ft_header.h"

void	ft_map(int n, int i, char *map)
{
	int		c;
	int 	j;
	
	c = 0;
	j = 0;
	while (i--)
	{
		map[j] = '.';
		if (c == n)
		{

			map[j] = '\n';
			c = -1;
		}
		c++;
		j++;
	}
}