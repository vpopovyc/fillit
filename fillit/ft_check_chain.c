#include "ft_header.h"

int		ft_check_chain(char *buf)
{
	int		j;
	int		i;

	j = 0;
	i = -1;
	while (buf[++i])
	{
		if (buf[i] == '#')
		{
			if (buf[i + 1] == '#')
				j++;
			if (buf[i + 5] == '#')
				j++;
		}
	}
	if (j == 3 || j == 4)
		return (1);
	return (0);
}
